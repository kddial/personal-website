<h1>Using Flexbox Gap for uniform spacing</h1>

<p>
  Using gap inside flexbox is a simple solution to give you uniform
  spacing between elements. Previously I would use margins on all
  elements except for first and last child, which was a clunky
  solution. The gap property was originally introduced for CSS Grids
  but it has been brought over for flexbox support since 2021 (see
  sources).
</p>

